# Repository on git hub
#   1. Github sign in
#   2. Click on new repository
#   3. Name the repository
#   4. Add description (optional)
#   5. Set it to public or private
#   6. Click on create repository
#   7. Add readme.txt file to the repository, and commit the changes


# make same space locally
#   1. Local folder (no space and special character)
#   2. Clone the repo - using source tree
#   3. In source tree - click on clone and paste the url(*.git) of the repo
#   4. Add the local folder path created in step 1
#   5. Click on clone
#  .git folder will start appearing in the local folder & readme.txt

# add new code
#   1. Create new branch in remote
#   2. Locally in source tree - Click on fetch to get the new branch
#   3. Click on checkout to switch to the new branch
#   4. Add new code in the local folder (trial.py)
#   5. In source tree - click on stage all, then commit and push the changes to remote