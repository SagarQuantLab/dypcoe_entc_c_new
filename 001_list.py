# List - Mutable, index, ordered, [], duplicates allowed
my_list = [10, 20, 30, 40, 'apple', 40]

# accessing first element of list
print(my_list[0])

# accessing first two elements
print(my_list[0:2])

# last element of list
print(my_list[-1])

# reverse my list
my_list.reverse()
print(my_list)

# value change
my_list[1] = 200
print(my_list)

# sort the list
my_list = [1, 5, 8, 9, 3]
my_list.sort()
print(my_list)